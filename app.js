// 変数名の前にletをつけることで、変数を宣言出来る
// let 変数名 = 内容 ;
let hello = 'Hello World';
alert(hello);


// 整数を代入する
let int1 = 1;

// 負数を代入する
let int2 = -10;

// 小数点を代入する
let float1 = 3.14;

// 文字列を代入する
let str1 = 'JavaScriptを覚えよう';


// 四則演算
// 足し算
alert(4 + 3);

// 引き算
alert(8 - 5);

// 掛け算
alert(2 * 6);

// 割り算
alert(10 / 2);